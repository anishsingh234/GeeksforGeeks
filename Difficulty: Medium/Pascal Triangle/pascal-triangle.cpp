class Solution {
  public:
    vector<int> nthRowOfPascalTriangle(int n) {
        // code here
         vector<int> result;
         
         long long ans = 1;
          result.push_back(ans);
         for(int i=1; i<n; i++){
             ans=ans*(n-i);
             ans=ans/i;
             result.push_back(ans);
         }
         return result;
    }
};



//{ Driver Code Starts.

void printAns(vector<int> &ans) {
    for (auto &x : ans) {
        cout << x << " ";
    }
    cout << "\n";
}

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        auto ans = ob.nthRowOfPascalTriangle(n);
        printAns(ans);

        cout << "~" << endl;
    }
    return 0;
}

